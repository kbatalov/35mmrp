Алкоголь<br>
</a></div>
<div class="accordion-body collapse" id="collapseEight">
<div class="accordion-inner"><div align="justify">&emsp;
    Идея принадлежит американскому химику Лестеру Хатту (Lester Hutt ). Он придумал, как представить алкогольные напитки в ином свете. Для этого он фотографирует алкоголь под микроскопом с 1000-кратным увеличением.
Чтобы сделать фотографию, каплю алкоголя капают на стекло, а затем высушивают в безвоздушном пространстве (шта?). Иногда на этот процесс уходит несколько месяцев. Напитки снимают на винтажную 35-мм камеру под микроскопом с 1000-кратным увеличением. Для удачного снимка может потребоваться пара сотен попыток. </div>
