Типографя слез<br>
</a></div>
<div class="accordion-body collapse" id="collapseSeven">
<div class="accordion-inner"><div align="justify">&emsp;
    Однажды фотограф Роза-Линн Фишер задумалась: могут ли слезы печали отличаться от слез радости? Так пришла идея посмотреть на них под микроскопом. За несколько лет было исследовано 100 видов слез - собственных, а также которые собирали волонтеры и даже младенческих. Оказалась что когда мы смеемся до слез, наши слезы получаются не похожими на слезы горя. Проект Роз-Линн Фишер назвала <b>"Типография слез"</b>.Идея создания фотопроекта возникла в трудный период жизни, когда фотограф потеряла двух важных для неё людей. Взглянув на свои слёзы под микроскопом, Роуз-Линн Фишер была удивлена тому, что некоторые из слез, фактически, похожи на снимки земли, сделанные с высоты птичьего полёта.Причём, она обнаружила разницу между рисунком линий и фигур. </div><blockquote><small><cite title="Роза-Линн Фишер afrnjhs cktps">Есть много факторов, от которых зависит внешний вид каждого изображения, в том числе: вязкость слезы, химия, настройка микроскопа, и, то как я обрабатываю изображения впоследствии.</cite></small></blockquote></div>
