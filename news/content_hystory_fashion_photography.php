<div class="accordion-inner"><div align="left">&emsp;
Джон Ролингс родился в штате Огайо в 1912 году. Он учился в местном университете Уэсли, а после окончания в 1930 году, переехал в Нью-Йорк.После выполнения своей первой работы, он стал внештатным работником магазина, занимаясь витринами. Это тот период, когда он обнаружил в себе интерес к фотографии.В 1930-х до начала 1940-х, он имел список его любимых моделей, с которыми он хотел бы работать, который включал в себя Дану Дженни, Хелен Бенет и Бетти Маклахен. Тем не менее, одной из его самых любимых была Мег Мунди, обнаруженная в зале ожидания на студии CBS.<br>&ensp;
В 1936 году он был нанят в Conde Nast, в качестве организатора съёмок и ассистента фотографов Сесил Битона и Хорста П.Хорст, а вскоре, он заменил их обоих. В сентябре того же года, его первые фотографии были опубликованы в Vogue. Ролингс изучал новые методы фото съёмки и освещения, экспериментируя с зеркалами, и сочетая естественные и искусственные источники света. Его работы были впечатляющими, и вскоре, его работы появились в британских, французских и американских изданиях Vogue. В 1945 году он вернулся в Нью-Йорк и открыл свою собственную студию, выбрав место с достаточным количеством естественного освещения, а также просторное для фото съёмок  и работы с моделями. К 1960 году он начал больше работать на американское издание Vogue, а также брал больше заказов на рекламные кампании. Ролингс открыл новый взгляд на <b>fashion-фотографии</b>, подчеркивая американский образ жизни.<br>&ensp;
Он был также одним из первых фотографов, которые автоматически ассоциировались с голливудскими знаменитостями. Провёл свои последние годы жизни с модельером Адри, и умер в 1970 году в возрасте 58 лет. Пожертвовал свой архив институту модных технологий, который был опубликован под названием John Rawlings: 30 years in Vogue.<br>&ensp;
Сегодня, Ролингс считается одним из самых плодовитых фотографов 20-го века, с более чем 200 проектов для Vogue и Glamour, и одним из наиболее важных fashion-фотографов, который позволил совместить красоту с ясностью, и создавать шедевры из рекламных кампаний</div></div>
