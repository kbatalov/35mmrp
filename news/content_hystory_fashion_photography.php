Фотокарьеру Брюс Вебер (Bruce Weber) начал не как фотограф, а как модель.Набравшись опыта, Вебер стал фотографировать сам, но его путь к фотографическому первым, кто признал талантливого фотохудожника, была фотограф Дайана Арбус.Она посоветовала мне пойти на курсы фотографии Лизетт Модел».<br>&ensp;  
