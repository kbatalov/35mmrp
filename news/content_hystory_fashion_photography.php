<div class="accordion-body collapse" id="collapseSixteen">
<div class="accordion-inner"><div align="left">&emsp;
Фотографировать Бэйли начал в детстве, но серьезно увлекся фотографией во время службы в армии. В это время открыл для себя Анри Картье-Брессона, стал постоянно читать «Life» и другие фотожурналы. В 1957 году купил себе первый фотоаппарат. «Постепенно перспектива стать фотографом становилось менее отдаленной, возможно даже достижимой» — писал он позднее.<br>&ensp; 
Вернувшись из армии в 1958 году, Дэвид Бэйли всерьез занялся фотографией и через два года стал знаменит, как и  модели: группа Beatles и Rolling Stones, певицы Мариана Фейтфулл и Сэнди Шоу, актрисы Катрин Денев и Джералдина Чаплин, актеры Питер Селлерс и Майкл Кэйн, модели Джин Шримптон и Пенелопа Три... Стиль фотографа, производивший впечатление в 1960-е годы, не изменился за сорок с лишним лет  карьеры: черно-белые фотографии, минимализм, графичность, контраст.<br>&ensp; 
