<div class="accordion-inner"><div align="left">&emsp;
Ги Бурден (Ги Луи Банарес) родился в Париже 2 декабря 1928 года, сразу после чего от него отказалась родная мать. Воспитывала приемная мать, чью фамилию он взял, его отношения с женщинами всегда были сложными. Дело в том, что жена, а также две другие  подруги покончили жизнь самоубийством, а самым мягким эпитетом, характеризующим обращение Бурдена с моделями, можно назвать «бесцеремонно». Увлечение фотографией началось во время военной службы в Сенегале в 1948-1949 годах.<br>&ensp; 
Возвращается в Париж в 1950-м познакомился с Маном Рэем (Эммануэлем Радницким), который увидел в Ги потенциал. Ман Рэй организовал первую персональную выставку Ги, а также помог наработать материал для фотовернисажа 1953 года. Пару лет спустя Ги Бурдена пригласили на работу в журнал Vogue Paris, где  оставался штатным фэшн-фотографом практически всю жизнь.Непростой характер художника отталкивал людей, да и сам Ги предпочитал общаться только с приятными ему людьми.<br>&ensp; 
Так, в 1985 году отказался от присужденной министерством искусств Франции премии. Ги Бурден скончался от рака 29 мая 1991 года в Париже. Согласно завещанию фотографа,  все работы должны были быть уничтожены после смерти, но, так как дома хранилась лишь часть негативов, сделать этого не удалось. Все дела покойного отца ведет сын Самюэль.</div><blockquote><small>(1928 г. - 29 марта 1991 г., Париж, Франция)<cite title="(1928 г. - 29 марта 1991 г., Париж, Франция)"></cite></small></blockquote>  
