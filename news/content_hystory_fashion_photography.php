Начал  карьеру фотографа в 1943 году, работая ассистентом художественного редактора журнала «Vogue» <a href="https://ru.wikipedia.org/wiki/%D0%9B%D0%B8%D0%B1%D0%B5%D1%80%D0%BC%D0%B0%D0%BD,_%D0%90%D0%BB%D0%B5%D0%BA%D1%81%D0%B0%D0%BD%D0%B4%D1%80" style="text-decoration: none" rel="nofollow">Александра Либермана</a>. 26-летний дизайнер имел собственное представление о том, что такое обложка, но не находил понимания у штатных фотографов журнала. Тогда Либерман посоветовал взять в руки фотокамеру и 1 октября 1943 года на обложке «Vogue» появилась первая фотография Ирвина Пенна. Эту дату можно считать вторым днем рождения.<br>&ensp; 
Большим успехом пользовалась серия фотографий привезенных Пенном из командировки в Перу. Студия, которую  арендовал у местного фотографа, выглядела экзотично: полы из каменной плитки, рисунки на стенах,  непонятный столик, на который облокачивались позирующие модели. Фотограф сделал в этой студии 200 портретов местных жителей, в том числе «Дети из города Куско» («Cuzco Children»). Другой проект Ирвина Пенна представлял собой серию портретов представителей различных профессий, обличенных в рабочую униформу и с атрибутами профессии в руках. Каждая модель на белом фоне и освещалась со стороны: это освещение характерно для портретов. В 1953 году, открывая собственную студию.<br>&ensp; 
Создаёт произведения искусства, снимая неказистые объекты — груду окурков или набор на первый взгляд несовместимых предметов. Любимый жанр — портрет. Пенн работает самозабвенно, увлекая за собой моделей. За время существования фотографии, портретистов, способных вызвать у модели подобные чувства, можно в буквальном смысле пересчитать по пальцам.</div><blockquote><small>Съемка пирожного тоже может быть искусством (1917 — 7 октября 2009, Нью-Йорк)<cite title="(1917 — 7 октября 2009, Нью-Йорк)" style="text-decoration: none"></cite></small></blockquote> 
