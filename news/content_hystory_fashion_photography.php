                                                <h2><center>Золотой век 1940 - 1959</center></h2>
<div align="left">&emsp;
<b>Fashion-фотография</b> отходит от съемки исключительно одежды, фотографы создают образы с психологическим подтекстом, отражающим собственное восприятие окружающего мира. Фотографы отказались и от классического единства модели, декора и одежды. Такие талантливые фотографы-новаторы, как Ричард Аведон (Richard Avedon) или Ирвин Пенн (Irving Penn), изменили мир fashion-фотографии. Начали создавать фотографии с удивительным освещением, экзотическими задними планами, вычурными позами и нескрываемым чувством юмора.</div>
