Родился в 1918 году в Лос-Анджелесе и переезжал с родителями, ирландскими эмигрантами, с места на место десятки раз, пока в 1932 году семья не осела в Сан-Франциско. В 1946-м Генри Кларк, декоратор витрин из Оукленда, берет полугодовой отпуск  и переезжает в Нью-Йорк. Вскоре уже работает в издательском доме <a href="https://ru.wikipedia.org/wiki/Cond%C3%A9_Nast" style="text-decoration: none" rel="nofollow">«Condé Nast»</a> на должности ассистента по аксессуарам и, наблюдая за работой великих Хорста и Сесила Биттона, находит истинное призвание – фотографию. Генри научился обращаться с казенной камерой и посетил несколько уроков, которые в те времена давал пионер графического дизайна в Америке, знаменитый <a href="https://ru.wikipedia.org/wiki/%D0%91%D1%80%D0%BE%D0%B4%D0%BE%D0%B2%D0%B8%D1%87,_%D0%90%D0%BB%D0%B5%D0%BA%D1%81%D0%B5%D0%B9_%D0%92%D1%8F%D1%87%D0%B5%D1%81%D0%BB%D0%B0%D0%B2%D0%BE%D0%B2%D0%B8%D1%87" style="text-decoration: none" rel="nofollow">Алексей Бродович</a>, а дальше судьба распоряжалась сама.<br>&ensp;  
В 1951 году Кларк подписал контракт, разрешющий снимать сразу для трех изданий – французского, английского и американского Vogue. В съемках Кларка  не появятся «нелепые спортивные туфли». Уличной моды там тоже не будет и следа. Зато настоящей экзотики предостаточно,  в 1960-е и 1970-е. Для Vogue это эпоха дальних странствий и великих открытий: Ливан и Антарктида, Бали и Иордания, остров Тасмания и Мертвое море.<br>&ensp;  
Когда 74-летний Кларк последний раз появился в лондонской редакции Vogue в 1992 году, забрал несколько оставшихся там ещё с 1950-х годов негативов. С этой славной декадой Кларк ассоциируется безошибочно и моментально. В последующие тридцать лет карьера была успешной, но в послевоенноммире, он показал, какими тонкими и изящными могут быть талии и лица.<br>&ensp;  
