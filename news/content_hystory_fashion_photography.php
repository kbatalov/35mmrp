<div class="accordion-heading"><a class="accordion-toggle" href="#collapseFour" data-toggle="collapse" data-parent="#accordion2" style="text-decoration: none"><br>
Хорост пи Хорст (Horst P. Horst)<br>
</a></div>
<div class="accordion-body collapse" id="collapseFour">
<div class="accordion-inner"><div align="left">&emsp;
Хорст на 100% студийный фотограф. Его стиль внимание к деталям положения тела модели, использование драматического освещения и  реквизита. В стилистике Хорста прослеживается увлечение сюрреализмом и греческими канонами физической красоты тела. Модели на фотографиях часто ассоциируются с античными изваяниями: Хорст даже заставлял девушек ходить в Лувр, чтоб проникались там пластикой и изящной грациозностью. Как считал фотограф, главная техническая проблема, которую приходится преодолевать коллегам, – это зажатость модели; с остальным  можно с лёгкостью справиться. Внимание Хорст уделял освещению: при этом сам разработал новаторский для своего времени способ создания светового рисунка, который придавал снимку трёхмерность и драматический эффект. Источники света располагал вверху по диагонали, создавая световой рисунок, плотно обтекающий модель. Благородство и изящество линий в фотографиях выдают осведомлённость Хорста в вопросах теории и практики искусства. В течение  жизни Хорст практически использовал фильтров. Большинство его фотографий сняты на черно-белую пленку, а если он и делал цветные фото, то они отличались особой монохромностью. В основном цвет можно встретить в его интерьерных снимках.<br>&ensp;
Одной из самых известных работ Хорста считается фотография под названием «The Mainbocher Corset» (1939 год). До сих пор этот незабываемый образ используется дизайнерами, такими как <a href="http://www.donnakaran.com/" style="text-decoration: none" rel="nofollow"> Donna Karan </a>, например, в качестве источника вдохновения при создании собственных коллекций. Этот же образ позаимствовала Madonna при съемке  клипа 1990 года на  песню <a href="https://www.youtube.com/watch?v=GuJQSAiODqI" style="text-decoration: none" rel="nofollow">Vogue</a>  (режиссёр – <a href="https://ru.wikipedia.org/wiki/%D0%A4%D0%B8%D0%BD%D1%87%D0%B5%D1%80,_%D0%94%D1%8D%D0%B2%D0%B8%D0%B4" style="text-decoration: none" rel="nofollow">Дэвид Финчер</a>).<br>&ensp; Ещё одним известным примером работ мастера является фотопортрет 1942 года, на котором запечатлена <a href="https://ru.wikipedia.org/wiki/%D0%94%D0%B8%D1%82%D1%80%D0%B8%D1%85,_%D0%9C%D0%B0%D1%80%D0%BB%D0%B5%D0%BD" style="text-decoration: none" rel="nofollow">Марлен Дитрих</a> в задумчивости: эта работа стала классикой гламурного портрета. По отзывам актрисы, никто другой из фотографов так не белил кожу и не скрадывал даже малейшие недостатки фигуры.
    В истории фотографии Хорст П. Хорст сыграл уникальную роль, став одним из родоначальников фэшн-фотографии и одним из создателей студийного ню.</div>
