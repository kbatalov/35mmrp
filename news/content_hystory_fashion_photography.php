Первая книга Деборы "Wallflower" вышла в 1978 году,  позднее издала еще две книги "Newport Remembered" и "Studio St. Petersburg". Так же принемала участие в выставках проходящих в галереях и музеях разных стран. В 1989 году была удостоина награды Lifetime Award for Fashion Photography от некоммерческой организации Fashion Group за  достижения в <b>fashion-фотографии</b>. В 1998 приз Альфреда Эйзенштадта за лучшую журнальную фотографию и Infinity Award в номинации "Прикладная фотография" от Международного центра фотографии в Нью-Йорке.<br>&ensp;  
