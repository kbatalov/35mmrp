Эдвард Стайхен или Стейхен (Edward Steichen)<br>
</a></div>
<div class="accordion-body collapse" id="collapseTwo">
<div class="accordion-inner"><div align="left">&emsp;
Крупнейший американский художник и фотограф. Поначалу Стейхен приверженец пикториальной фотографии, в которой достиг больших успехов: в 2006 году фотография <a href="http://35mmrp/one-million.php" style="text-decoration: none"> «Озеро в лунном свете» </a> продана на аукционе за 2 928 000 долларов. После Первой мировой войны под влиянием друга <a href="https://ru.wikipedia.org/wiki/%D0%A1%D1%82%D0%B8%D0%B3%D0%BB%D0%B8%D1%86,_%D0%90%D0%BB%D1%8C%D1%84%D1%80%D0%B5%D0%B4" style="text-decoration: none" rel="nofollow">Альфреда  Стиглица</a> Стейхен отошел от пикториализма.<br>&ensp;
Эдвард Стейхен известен так же, как организатор международных выставок. Самой знаменитой была выставка «Род человеческий» («The Family of Man»), премьера которой состоялась в Нью-Йорке в 1955 году. В выставке были представлены работы двухсот семьдесят трех фотографов из шестидесяти восьми стран. Среди экспонатов были работы знаменитых и никому не известных авторов. Экспозиция «Род человеческий» выставлялась в сорока странах, её посетило около десяти миллионов человек.<br>&ensp;
В 1923 году стал главным фотографом сразу в двух журналах, выпускаемых издательством Conde Nast — Vanity Fair и Vogue. Для Vogue Штайхен делал снимки произведений знаменитых кутюрье: <a href="https://ru.wikipedia.org/wiki/%D0%A8%D0%B0%D0%BD%D0%B5%D0%BB%D1%8C,_%D0%9A%D0%BE%D0%BA%D0%BE" style="text-decoration: none" rel="nofollow">Коко Шанель</a>, <a href="https://ru.wikipedia.org/wiki/%D0%9B%D0%B0%D0%BD%D0%B2%D0%B5%D0%BD,_%D0%96%D0%B0%D0%BD%D0%BD%D0%B0" style="text-decoration: none" rel="nofollow">Жанны Ланвен</a>, <a href="https://ru.wikipedia.org/wiki/%D0%A1%D0%BA%D0%B8%D0%B0%D0%BF%D0%B0%D1%80%D0%B5%D0%BB%D0%BB%D0%B8,_%D0%AD%D0%BB%D1%8C%D0%B7%D0%B0" style="text-decoration: none" rel="nofollow">Эльзы Скиапарелли</a>. На страницах Vanity Fair публиковались портреты: <a href="https://ru.wikipedia.org/wiki/%D0%9A%D1%83%D0%BF%D0%B5%D1%80,_%D0%93%D1%8D%D1%80%D0%B8" style="text-decoration: none" rel="nofollow">Гэри Купера</a>, <a href="https://ru.wikipedia.org/wiki/%D0%94%D0%B8%D1%82%D1%80%D0%B8%D1%85,_%D0%9C%D0%B0%D1%80%D0%BB%D0%B5%D0%BD" style="text-decoration: none" rel="nofollow">Марлен Дитрих</a>, <a href="https://ru.wikipedia.org/wiki/%D0%A4%D1%80%D0%B5%D0%B4_%D0%90%D1%81%D1%82%D0%B5%D1%80" style="text-decoration: none" rel="nofollow">Фреда Астера</a>.</div><blockquote><small>Когда я начал интересоваться фотографией, я думал, что она принадлежит к одному из изящных искусств. Сегодня я придерживаюсь совершенно другого мнения. (1879 - 1973, США) <cite title="1879 - 1973, США)"></cite></small></blockquote> 
