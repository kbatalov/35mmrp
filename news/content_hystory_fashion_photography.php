<div class="accordion-heading"><a class="accordion-toggle" href="#collapseTwenty-two" data-toggle="collapse" data-parent="#accordion4" style="text-decoration: none"><br>
Артур Элгорт (Arthur Elgort)<br>
</a></div>
<div class="accordion-body collapse" id="collapseTwenty-two">
<div class="accordion-inner"><div align="left">&emsp;
Артур Элгорт (Arthur Elgort) появился на свет в Бруклине 8 июня 1940 года. Мальчик был любознательным и любил рисовать. Наверно поэтому после окончания школа Стувесант, Артур поступил в колледж Хантер, где углубленно изучал техники рисования. После окончания колледжа жизнь свела его с Гусом Петерсоном, который стал учителем, и толковым наставником. Кажущаяся небрежность привлекала внимание главных редакторов модных журналов. По мимо работы в Vogue, Артур снимал для Rolling Stones, Glamour, GQ и конечно Mademoiselle, создателей которого  являлся.<br>&ensp;  
Благодаря работе в Vogue Артур побывал в разных уголках мира и поработал на разные издательства журнала, в том числе и в России, где ему нравится. В Петербурге Артур снимал <a href="https://ru.wikipedia.org/wiki/%D0%A1%D0%B5%D0%BC%D0%B0%D0%BD%D0%BE%D0%B2%D0%B0,_%D0%9D%D0%B0%D1%82%D0%B0%D0%BB%D1%8C%D1%8F" style="text-decoration: none" rel="nofollow">Наталью Семанову</a>, а в Москве <a href="https://ru.wikipedia.org/wiki/%D0%91%D0%B0%D1%80%D1%8B%D1%88%D0%BD%D0%B8%D0%BA%D0%BE%D0%B2,_%D0%9C%D0%B8%D1%85%D0%B0%D0%B8%D0%BB_%D0%9D%D0%B8%D0%BA%D0%BE%D0%BB%D0%B0%D0%B5%D0%B2%D0%B8%D1%87" style="text-decoration: none" rel="nofollow">Михаила Барышникова</a>. Кстати работа с Барышниковым не единственная съемка актеров балета, ведь это еще одна страсть Артура, которая отражена во многих его работах. Не редко модели Артура принимают танцевальные позы или застывают на снимка во время полета. Так же Артур писатель, у которого уже две книги Personal Fashion Photograph (1983) и Models Manual (1994).</div></div>
