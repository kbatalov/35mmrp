Адольф де Мейер (Adolphe de Meyer)<br>
</a></div>
<div class="accordion-body collapse" id="collapseOne" style="height: 0px;">
<div class="accordion-inner"><div align="left">&emsp;
В 1898-1899 годах Адольф де Мейер стал членом Королевского фотографического общества Великобритании и присоединился в ассоциации британских фотографов-пикторалистов "Linked Ring". А чуть позже приобретает  линзы фирмы "Pinckerton Smith", работа которых основана на совмещении фокуса в центре с градуированной диффузией по периметру. Эта специфика, вместе с характерной для де Мейера техникой заднего освещения, создала то "сияние", которое принесло известность фотографиям.<br>&ensp;
Сделал первую в мире фотографию для журнала VOGUE. Использовал статичные позы, контровую подсветку и мягкорисующую оптику. В 1914 уехал в США где снимал для VOGUE, Vanity Fair и Hiper Bazar. Среди моделей Мейера: король <a href="https://ru.wikipedia.org/wiki/%D0%AD%D0%B4%D1%83%D0%B0%D1%80%D0%B4_VII" style="text-decoration: none" rel="nofollow">Эдуард VII</a>, <a href="https://ru.wikipedia.org/wiki/%D0%9C%D1%8D%D1%80%D0%B8_%D0%9F%D0%B8%D0%BA%D1%84%D0%BE%D1%80%D0%B4" style="text-decoration: none" rel="nofollow">Мэри Пикфорд</a>, <a href="https://ru.wikipedia.org/wiki/%D0%9D%D0%B5%D0%BB%D0%BB%D0%B8_%D0%9C%D0%B5%D0%BB%D1%8C%D0%B1%D0%B0" style="text-decoration: none" rel="nofollow">Нелли Мельба</a>, светские дамы эпохи, и жена - баронесса Ольга де Мейер. Большинство работ Мейера погибло в годы войны. Первый официальный фотограф Vogue. 
