<h2><center>Начало 1911 - 1939</center></h2>
<div align="left">&emsp;
<b>Fashion-фотография</b> уже давно превратилась в самостоятельное направление фотоиндустрии. Она окружает повсюду – видим яркие, и соблазнительные образы в журналах, на билбордах и в сети. С помощью неё преподносятся современные тенденции в мире моды и искусства. Fashion-фотография, которая развивалась в течение десятилетий, сформировала собственное восприятие эстетики и красоты.<br>&ensp; 
Сегодня <b>fashion-фотография</b> стала востребованным и дорогостоящим направлением, в которое вовлечены фотографы, стилисты, модели, визажисты и редакторы журналов. Fashion-фотография начала зарождаться в конце XIX – начале XX века благодаря популярности журналов. На страницах Harper Bazar и Vogue, вместо привычных гравюр и графики начали появляться первые черно-белые фотоизображения, благодаря которым редакторы журналов пытались привлечь дополнительную аудиторию читателей.<br>&ensp; Соперничавшие друг с другом журналы Vogue и Harper’s Bazaar стали пионерами в этой сфере. Они первыми стали размещать фотоизображения на разворотах и обложках. В конце XIX века еще пользовалась популярностью пикториальная фотография, которая заимствовала традиции живописи. Фотографы-пикториалисты старались придать снимкам нотки загадочности, используя нерезкие силуэты и подтекст. Разумеется, пикториалисты уделяли внимание и образу женской привлекательности, и обнаженной натуре.</div>
<blockquote><small>Например, первой коллекцией модных снимков считается маленькая книжка, опубликованная еще в 1856 году Адольфом Брауном. В книге Адольфа Брауна было опубликовано 300 фотоизображений графини Вирджинии Кастильоне. Причем запечатлена она была в самых разных нарядах. Это можно считать первым примером модной фотографии<cite title="1856 год Адольф Браун"></cite></small></blockquote> 
