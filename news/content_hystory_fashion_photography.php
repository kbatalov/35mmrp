<div class="accordion-heading"><a class="accordion-toggle" href="#collapseFive" data-toggle="collapse" data-parent="#accordion2" style="text-decoration: none"><br>
Мартин Мункаши (Martin Munkacsi)<br>
</a></div>
<div class="accordion-body collapse" id="collapseFive">
<div class="accordion-inner"><div align="left">&emsp;
Настоящее имя  Мартон Мермельштейн. Родился в 1896 году в тогда еще Австро-Венгерской империи и подростком начал печататься в будапештских журналах и газетах, делая репортажи о футболе и автогонках. После того как газеты начали публиковать фотографии, он открыл для себя фотокамеру. Уже его первые снимки свидетельствовали об индивидуальном почерке, который он позже доведёт до совершенства: в то время, когда фотография ещё не совсем освободилась от своей тяжеловесной статичности, он уже делал снимки, отличавшиеся жизненной свежестью, а также уникальной для своего времени динамикой.<br>&ensp;
Приехав в 1928 году в Берлин, Мункаши оказался в нужное время на нужном месте. Мункаши любил снимать сцены повседневной жизни и был великолепен в сфере спортивной фотографии — там, где важно было умение «поймать» движение, действие, к чему у него явный талант. Работы печатали в больших журналах, таких как die Dame , Uhu и в иллюстрированном журнале <a href="https://de.wikipedia.org/wiki/Berliner_Illustrirte_Zeitung" style="text-decoration: none" rel="nofollow">Berliner Illustrierte Zeitung</a>, выходившем самым большим тиражом в мире - больше миллиона экземпляров.<br>&ensp; 
Его фотографии знакомых и незнакомых женщин наполнены тонкой эротикой. Можно буквально почувствовать, как его глаза, ведомые красотой, находили графически волнующие композиции. Благодаря этим снимкам Мункаши стал  представителем «нового видения» и модерна в фотографии.<br>&ensp;
21-го марта 1933 года мастер фотографировал путь нового рейхсканцлера <a href="https://ru.wikipedia.org/wiki/%D0%93%D0%B8%D1%82%D0%BB%D0%B5%D1%80,_%D0%90%D0%B4%D0%BE%D0%BB%D1%8C%D1%84" style="text-decoration: none" rel="nofollow">Адольфа Гитлера</a> в Потсдамскую гарнизонную церковь. Как результат - целый журнальный разворот о Гитлере и <a href="https://ru.wikipedia.org/wiki/%D0%93%D0%B5%D0%B1%D0%B1%D0%B5%D0%BB%D1%8C%D1%81,_%D0%99%D0%BE%D0%B7%D0%B5%D1%84" style="text-decoration: none" rel="nofollow">Геббельсе</a> в то время, когда нацисты уже начали охоту на евреев. Покидает Германию в 1934 году, пересекает Атлантику и, еще не доехав до Нью-Йорка, получает задание от журнала Harper”s Bazaar.<br>&ensp;
Его фотографии манекенщиц, которых он заставлял прогуливаться зимой вдоль пляжа, привносят в мир американской моды  новые настроения — спортивности, молодой энергии, жизненной силы, движения.Через год Harper”s Bazaar уже подписывает с ним долгосрочный контракт. Фотографирует <a href="https://ru.wikipedia.org/wiki/%D0%A4%D1%80%D0%B5%D0%B4_%D0%90%D1%81%D1%82%D0%B5%D1%80" style="text-decoration: none" rel="nofollow">Фреда Астера</a> в танце и <a href="https://ru.wikipedia.org/wiki/%D0%94%D0%B6%D0%BE%D0%B0%D0%BD_%D0%9A%D1%80%D0%BE%D1%83%D1%84%D0%BE%D1%80%D0%B4" style="text-decoration: none" rel="nofollow">Джоан Кроуфорд</a> у бассейна, работает вместе с легендарным дизайнером журнала <a href="https://ru.wikipedia.org/wiki/%D0%91%D1%80%D0%BE%D0%B4%D0%BE%D0%B2%D0%B8%D1%87,_%D0%90%D0%BB%D0%B5%D0%BA%D1%81%D0%B5%D0%B9_%D0%92%D1%8F%D1%87%D0%B5%D1%81%D0%BB%D0%B0%D0%B2%D0%BE%D0%B2%D0%B8%D1%87" style="text-decoration: none" rel="nofollow">Алексеем Бродовичем</a>, чьё видение еще более усиливает эффектность его снимков. Высшими точками карьеры стали необычные портреты таких голливудских звёзд, как <a href="https://ru.wikipedia.org/wiki/%D0%94%D0%B6%D0%B8%D0%BD_%D0%A5%D0%B0%D1%80%D0%BB%D0%BE%D1%83" style="text-decoration: none" rel="nofollow"> Жан Харлоу</a>, <a href="https://ru.wikipedia.org/wiki/%D0%A5%D0%B5%D0%BF%D0%B1%D1%91%D1%80%D0%BD,_%D0%9A%D1%8D%D1%82%D1%80%D0%B8%D0%BD" style="text-decoration: none" rel="nofollow">Катрин Хепбёрн</a>,<a href="https://ru.wikipedia.org/wiki/%D0%93%D0%BE%D0%B2%D0%B0%D1%80%D0%B4,_%D0%9B%D0%B5%D1%81%D0%BB%D0%B8" style="text-decoration: none" rel="nofollow"> Лесли Ховард</a>,<a href="https://ru.wikipedia.org/wiki/%D0%A0%D0%B0%D1%81%D1%81%D0%B5%D0%BB%D0%BB,_%D0%94%D0%B6%D0%B5%D0%B9%D0%BD" style="text-decoration: none" rel="nofollow"> Джейн Рассел</a> и <a href="https://ru.wikipedia.org/wiki/%D0%94%D0%B8%D1%82%D1%80%D0%B8%D1%85,_%D0%9C%D0%B0%D1%80%D0%BB%D0%B5%D0%BD" style="text-decoration: none" rel="nofollow"> Марлен Дитрих</a>.<br>&ensp;
Долго эта счастливая полоса не продлилась. В холодное и чопорное послевоенное время звезда жизнелюба и специалиста по эротике закатилась: последовали личные неурядицы и трагедии (развод, смерть дочери, позже еще одна женитьба, закончившаяся разводом), карьерные ошибки (переход в Lady”s Home Journal), инфаркт, неудачные пробы в других сферах (кино, сценарии). Когда в 1963 году он умер от инфаркта. Стилистика повлияла на следующие поколения американских фотографов, работавших в сфере моды, — таких, как Ричард Аведон, который после смерти Мункаши написал, что Мункаши принес «вкус счастья, честности и любви к женщине в то, что до него было искусством безрадостным, лишенным любви, лживым».<br>&ensp;
    Умер в нищите. Произвел революцию в фешен фотографии, освободил фотографов и моделей от студий.</div></div>
