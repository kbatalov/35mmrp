<div class="accordion-body collapse" id="collapseFifteen">
<div class="accordion-inner"><div align="left">&emsp;
Хельмут родился в Берлине. Мать американка, а отец немецким евреем. Отец владел фабрикой по производству пуговиц, и семья была далеко не бедной. Во время учёбы в лицее, наступил момент, когда все ученики были поделены на арийцев и евреев. После разделения, отец забрал сына из этой школы и определили в американскую, где Хельмут не задержался. Учился без желания, и его исключили за лень, повышенный интерес исключительно к девочкам, плаванию и фотографии.<br>&ensp; 
Интерес к <b>фотографии</b> мальчик начал проявлять с двенадцати лет. Точнее с  момента, как познакомился с творчеством фотографа Иве. Приобрёл дешёвый фотоаппарат и занялся фотографией. На его увлечение имело влияние и то, что семья Ньютонов всегда выписывала большое число иллюстрированных журналов.
После войны Хельмут решил не покидать Австралию и открыл в Мельбурне собственную студию. В изданиях Queen, Playboy и Gardende Mode работал свободным фотографом. 
В 1948 году Ньютон женился на Джун Браун. Была актрисой, однако впоследствии (с 1970 года) стала известным фотографом. Её псевдонимом - Эллис Спринг.<br>&ensp;	
С 1881-го Хельмут жил периодически и в Лос-Анджелесе, и в Монте-Карло.  Хельмут считал, что женские эротические фантазии заключаются в господстве мужчин. Стремился сделать не жестокие эротические снимки, снимки, которые не унижают женщин. Одно время фотограф увлекался садомазохистскими образами.
