Младшая третья дочь в семье иммигрантов в 19 лет стала студенткой Калифорнийской школы изящных искусств. Талантливая девушка изучала направлений живописи – фигурная композиция, анатомия тела, рисование с натуры. Затем училась в Колумбийском университете, где Луиза изучала архитектуру и дизайн.<br>&ensp; 
В 1932 Луиза снимала местных жителей в Теннеси для развития способностей фотографа. После окончания университета, Луиза вышла замуж за не менее талантливого конструктора, который в дальнейшем изготавливал задние планы для фоторабот жены. После замужества банальная фамилия стала двойной, и простая девушка стала звать себя Луиза Даль-Вульф (Louise Dahl-Wolfe).<br>&ensp; 
Главным отличием фотографа от коллег по цеху стала любовь Луизы снимать на открытом воздухе, при естественном освещении.<br>&ensp; 
Мэри Джейн Рассел, любимая модель Луизы, по средним подсчетами запечатлена на 30% снимков. Работы Луизы повлияли на работу фотографов современности, как Ричард Аведон, Хорст Пауль Хорст и Ирвинг Пенн. А Милтон Грин - ассистентом. За 27лет в Нью-Йорке Луиза создала много рекламных проектов, баннеров и обложек. 
<br>&ensp;
С 1936 года Луиза работала в Harper’s Bazaar. Это было золотое время для журнала, популярность увеличивалась в геометрической прогрессии с каждым следующим номером. За короткий период журнал стал одним из самых читаемых у женщин.<br>&ensp;  
За 22 года сотрудничества  Луиза сделала 600 цветных фотографий и 86 обложек журнала, а еще черно-белые снимки, которые уже и не счесть. С 1958 по 1960 Луиза работала в Sports Illustrated и Vogue как внештатный фотограф.<br>&ensp;
