Сара Мун (Sarah Moon)<br>
</a></div>
<div class="accordion-body collapse" id="collapseTwenty-one">
<div class="accordion-inner"><div align="left">&emsp;
Настоящее имя французского фотографа Сары Мун (Sarah Moon) – Марьель Хадан (Marielle Hadengue). Родилась в Виши в 1941 году, вскоре  родители покинули оккупированную Францию и перебрались в Англию. Будучи подростком, Марьель училась рисованию, а затем стала работать фотомоделью haute couture в Лондоне и Париже (1960-1966).<br>&ensp;  
Увлекшись фотографией, Марьель снимала  коллег моделей. В 1970 году наконец, решила всё время посвятить фотографии и взяла псевдоним Сара Мун.
В 1972 году Сара Мун стала первой женщиной, снявшей календарь Pirelli. После  работы с Cacharel,  репутация выросла, тогда появились заказы Chanel, Dior, Comme Des Garcons и Vogue. «Если что-то вовсе плохо, то я отретуширую кадр, но лишь совсем немного. И я никогда не пытаюсь сделать женщин красивее. Мне не нужно этого делать. Они красивы, моё дело – работать со светом. Я не имею морального права осуждать людей, которые предпочитают работать именно так, но я полагаю, что это фальсифицирует подход к человеку», – говорит Мун.<br>&ensp;  
В 1979 году Sarah Moon занялась кинематографией.Вдохновителем являлся Сергей Эйзенштейн. Начинала карьеру в кино с рекламных роликов, но затем перешла к документальным фильмам. Среди работ как полнометражные, так и короткометражные фильмы. Фотографии Сары Мун выполнены в стиле старых фотографий и манере пикториализма.<br>&ensp;    
Сара Мун считает цвет только  помехой и занимается исключительно чёрно-белой фотографией.Снимки напоминают рисунки. В них объединяется фотография, живопись, игра света и тени и застывшая динамика. Многие снимки размыты и не четки, но так фотохудожница видит мир. Журналистам Сара Мун говорила, что в работах  старается зафиксировать изображение, а не передать историю, которую должен рассказывать снимок.
</div></div>
