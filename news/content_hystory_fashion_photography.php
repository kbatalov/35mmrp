<div class="accordion-heading"><a class="accordion-toggle collapsed" href="#collapseThirteen" data-toggle="collapse" data-parent="#accordion4" style="text-decoration: none"><br>
Уильям Кляйн (William Klein)<br>
</a></div>
<div class="accordion-body collapse" id="collapseThirteen" style="height: 0px;">
<div class="accordion-inner"><div align="left">&emsp;
После Второй Мировой Войны Уильям Кляйн (William Klein) — молодой американский солдат, мечтавший о карьере художника и скульптора, решил остаться в Европе. Переехав в Париж, учился в Сорбонне, потом работал в мастерской художника-авангардиста Фернана Леже.<br>&ensp;  
С 1954 по 1966 год работал в «Vogue». 
Одновременно обладал художественным чутьем и отсутствием страха в использовании нестандартных решений — эти два качества дали возможность оживить «Vogue», но и изменить отношение к <b>fashion-фотографии</b>. Либерман считал, что «Кляйн стоит в одном ряду с Хельмутом Ньютоном, Фрэнком Хорватом и Дэвидом Бейли».
В 1956 году  издал книгу «Нью-Йорк», за которой последовали книги о Риме (1960), Москве (1964) и Токио (1964). Вокруг книги о Нью-Йорке разразился скандал, фотографа обвинили в том, что снимки показывают американцев с негативной стороны, непривлекательными и агрессивными. До сих пор книга, напечатанная во многих странах мира, не издана в Америке.<br>&ensp; 
В середине 1960-х годов Кляйн занялся кинематографом и до 1980 года отошел от фотографии. В одном из интервью на вопрос в чем заключается разница между фотографией и кино, он ответил: <blockquote><small>В том, что в кино за тобой носят всю аппаратуру, а в фотографии ты должен носить все сам. Но есть, конечно, и другие отличия. В кино есть <b>история</b>. Фотографию гораздо тяжелее читать. Ты делаешь снимок, но люди не всегда его понимают. Считается, что фотография делается ради шутки, чтобы застать человека, ковыряющегося в носу, врасплох. В кино есть начало и конец, и зрителю гораздо легче сохранять концентрацию».<cite title="Ответ на вопрос по поводу фотографии" style="text-decoration: none"></cite></small></blockquote>С начала 1980-х Уильям Кляйн совмещает кинематограф и фотографию, снимая в на улице в местах  скопления людей и используя исключительно широкоугольную оптику.</div>
