<div class="accordion-heading"><a class="accordion-toggle" href="#collapseSix" data-toggle="collapse" data-parent="#accordion2" style="text-decoration: none"><br>
Эрвин Блюмендфельд (Erwin Blumendfeld)<br>
</a></div>
<div class="accordion-body collapse" id="collapseSix">
<div class="accordion-inner"><div align="left">&emsp;
Эрвин Блюменфельд родился в Берлине в обеспеченной еврейской семье. Рано увлекся искусством, в частности живописью и фотографией, после Первой мировой войны примкнул к дадаизму. Однако это увлечение  не обещало сделать богатым и знаменитым, поэтому, женившись, он перебрался в Амстердам, где в 1922 году открыл магазинчик торгующий женской кожгалантереей.<br>&ensp;
Слабая торговля заставила искать утешение в увлечениях юности, он снова обратился к фотографии, преобразуя по ночам торговые залы своего магазина в фотостудию. Именно там он создал основы своего стиля — реального и фантастического одновременно. Поначалу он фотографировал старые торговые манекены, позже им на смену пришли живые модели.<br>&ensp;
К середине 1930-х годов у Блюменфельда было уже трое детей, галантерейный бизнес, не приносивший своему хозяину ни денег, ни удовлетворения, талант и абсолютная уверенность в своих силах. В 1936 году бросает магазин и переезжает в Париж. Уже через год фотографии Блюменфельда появляются на обложках модных журналов, в конце 1930-х годов его популярность постоянно и стремительно растет, а вместе с ней и давно забытое финансовое благополучие.<br>&ensp;
В 1941 году Эрвин Блюменфельд был вынужден бежать в США, где он и оставался до конца жизни. Поначалу он делил фотоателье с Мартин Мункаши, позднее обзавелся собственным помещением. Блюменфельд стал сотрудничать с самыми популярными модными журналами «Harper`s Bazaar» и «Vogue». Он плодотворно и очень успешно работал в мире мода и рекламы до середины 1950-х годов. Лучше всего работу фотографа характеризует его же трудно переводимый афоризм «Beauty is not pretty» («Красивое не значит хорошенькое»). Этот девиз не потерял своей актуальности и много десятилетий спустя.<br>&ensp;
В середине 1950-х годов стиль журнальной фотографии отошел от той иносказательности, которую так любили Блюменфельд и другие фотозвезды первой половины XX столетия. Гламур стал более «прямым», эротика более открытой, красота — совсем «хорошенькой». Блюменфельд, никогда не скрывавший своего мнения, разругался с художественным директором издательства «Conde Nast» <a href="https://ru.wikipedia.org/wiki/%D0%9B%D0%B8%D0%B1%D0%B5%D1%80%D0%BC%D0%B0%D0%BD,_%D0%90%D0%BB%D0%B5%D0%BA%D1%81%D0%B0%D0%BD%D0%B4%D1%80" style="text-decoration: none" rel="nofollow">Александром Либерманом</a> и навсегда ушел из мира моды.
В 1978 году через восемь лет после смерти фотографа вышла в свет его автобиография с практически непереводимым названием «Eye to I». В некотором смысле она похожа на автобиографию Хельмута Ньютона — оба уделяют больше внимания своим детским сексуальным фантазиям, чем фотографии. В их книгах мало гламурных сплетен, хотя и есть едкие, язвительные замечания об индустрии моды. На этом сходство заканчивается за исключением одного момента — они оба достаточно талантливые писатели.<br>&ensp;
Он скончался 4 июля 1969 года, в возрасте 72 лет, во время его римских каникул.Основной стиль в его работах это многократное экспонирование, цветные фильтры, эксперименты с печатью.
