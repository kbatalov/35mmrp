<b>Интересные факты:</b><ul>
    <li>Скопин — один из старейших городов Рязанской области. Его окрестности были заселены ещё в <a href="https://ru.wikipedia.org/wiki/%D0%9D%D0%B5%D0%BE%D0%BB%D0%B8%D1%82" style="text-decoration: none" rel="nofollow">неолитическую эпоху</a>.</li> 
    <li>Интересна версия и о том, что название города связано с древнерусским мужским именем или прозвищем Скопа, которое в свою очередь, происходит либо от глагола «скопить» в значении «сберегать, копить, собирать». Так же <b>«скоп»</b> - это устаревший синоним слов «запас» или «склад».
    </li>
</ul>
