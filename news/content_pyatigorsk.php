<b>Интересные факты:</b><ul>
<li>Пятигорск является основным местом действия романа «Герой нашего времени» (<a href="https://ru.wikipedia.org/wiki/%D0%9B%D0%B5%D1%80%D0%BC%D0%BE%D0%BD%D1%82%D0%BE%D0%B2,_%D0%9C%D0%B8%D1%85%D0%B0%D0%B8%D0%BB_%D0%AE%D1%80%D1%8C%D0%B5%D0%B2%D0%B8%D1%87" style="text-decoration: none" rel="nofollow">М. Ю. Лермонтов</a>).</li>
<li>В Пятигорске самая высокая телебашня в Европе. Телебашня имеет высоту <b>113 метров</b>, но она установлена на вершине горы Машук, высота которой — <b>956 метров</b>. Таким образом, общая высота горы Машук и башни составляет <b>1069 метров</b> над уровнем моря.</li>
    <li>В честь города названа планета. <b>Пятигория</b> — малая планета 2192 Pyatigoriya.</li>        
</ul>
