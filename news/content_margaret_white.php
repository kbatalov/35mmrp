<h3>Военный корреспондент.</h3>
<blockquote><small>1904-1971 США</small></blockquote>
<div align="left">&emsp;	
Маргарет Бурк-Уайт (Margaret Bourke-White) — фотокорреспондент журнала Life. В 1941 году она побывала в СССР и присутствовала в Москве во время нападения Германии. Сделала много интересных снимков. Позже сопровождала американские войска в северной Африке, а затем — в Италии и Германии. Весной 1945 года Бурк-Уайт сопровождала американского генерала Джорджа Паттона в его поездке по Германии и посетила концентрационный лагерь Бухенвальд (Buchenwald), запечатлев свидетельства творящегося там ужаса.</div>
