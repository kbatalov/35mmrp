<h3>Военный корреспондент.</h3>
<blockquote><small>1898-1959 СССР</small></blockquote>
<div align="left">&emsp;	
Аркадий Самойлович Шайхет c 1924 года фотографировал для журналов «Огонек», «СССР на стройке», «Наши достижения», создав фотолетопись первых пятилеток. Во время Великой Отечественной войны был корреспондентом газеты «Фронтовая иллюстрация». Много снимал военные действия, в том числе под Москвой, под Сталинградом, на Курской дуге, при взятии Берлина. В послевоенные годы снова работал в журнале «Огонек».</div>
