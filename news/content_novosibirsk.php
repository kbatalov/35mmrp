<b>Интересные факты:</b><ul>
<li>В Новосибирске улица Планировочная одновременно и параллельна, и перпендикулярна сама себе и образует перекрёсток сама с собой.</li> 
<li>Первый человек, ступивший на Луну, американский астронавт <a href="https://ru.wikipedia.org/wiki/%D0%90%D1%80%D0%BC%D1%81%D1%82%D1%80%D0%BE%D0%BD%D0%B3,_%D0%9D%D0%B8%D0%BB" style="text-decoration: none" rel="nofollow">Нил Армстронг</a>, специально побывал в Новосибирске, чтобы увезти с собой горсть земли от стен дома, в котором жил и работал один из пионеров космонавтики <a href="https://ru.wikipedia.org/wiki/%D0%9A%D0%BE%D0%BD%D0%B4%D1%80%D0%B0%D1%82%D1%8E%D0%BA,_%D0%AE%D1%80%D0%B8%D0%B9_%D0%92%D0%B0%D1%81%D0%B8%D0%BB%D1%8C%D0%B5%D0%B2%D0%B8%D1%87" style="text-decoration: none" rel="nofollow">Юрий Кондратюк</a>, автор схемы полёта космического корабля к Луне, использованной для осуществления программы <a href="kipedia.org/wiki/Аполлон_(космическая_программа)" style="text-decoration: none" rel="nofollow">«Аполлон»</a>.</li>
<li>Город Новосибирск занял первое место в рейтинге альтернативных столиц России, составленном специалистами медиагруппы <a href="https://ru.wikipedia.org/wiki/%D0%A0%D0%91%D0%9A" style="text-decoration: none" rel="nofollow">«РосБизнесКонсалтинг»</a> на основании результатов опроса, проведённого в конце апреля — начале июня 2012 года.</li>
</ul>
